    # Never-edit paths
    never_edit: list[str] = []
    if overrides and overrides.never_edit:
        never_edit.extend(overrides.never_edit)
    for dir_name in sorted(scan.top_level_dirs):
        if dir_name.lower() in ("migrations", "migration", "vendor", "third_party", "thirdparty"):
            candidate = f"{dir_name}/"
            if candidate not in never_edit:
                never_edit.append(candidate)
    if never_edit:
        ne_lines = "\n".join(f"- `{p}`" for p in never_edit[:8])
        sections.append(render_markdown_section("Never edit", ne_lines))

    # Task hints from overrides
    if overrides and overrides.task_hints:
        hint_lines = "\n".join(
            f"- **{th.pattern}**: {th.hint}" for th in overrides.task_hints[:5]
        )
        sections.append(render_markdown_section("Task hints", hint_lines))

    return "\n\n".join(sections)


