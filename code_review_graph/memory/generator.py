def _render_stack(scan: RepoScan) -> str:
    lines: list[str] = []

    if scan.languages:
        lines.append(f"- **Languages**: {', '.join(scan.languages)}")
    else:
        lines.append("- **Languages**: not detected")

    if scan.framework_hints:
        lines.append(f"- **Frameworks**: {', '.join(scan.framework_hints)}")

    if scan.file_counts:
        counts_str = ", ".join(
            f"{lang} ({n})" for lang, n in sorted(scan.file_counts.items(), key=lambda x: -x[1])
        )
        lines.append(f"- **File counts**: {counts_str}")

    if scan.config_files:
        lines.append(f"- **Config files**: {', '.join(sorted(scan.config_files))}")

    if not lines:
        lines.append("Stack not detected — see detection notes.")

    return "\n".join(lines)


def _render_top_level(scan: RepoScan) -> str:
    if not scan.top_level_dirs:
        return "No top-level directories detected."
    lines = [f"- `{d}/`" for d in sorted(scan.top_level_dirs)]
    return "\n".join(lines)


def _render_key_locations(scan: RepoScan) -> str:
    lines: list[str] = []

    if scan.source_dirs:
        lines.append(f"- **Source**: {', '.join(f'`{d}/`' for d in scan.source_dirs)}")
    else:
        lines.append("- **Source**: not clearly identified (may be at repo root)")

    if scan.test_dirs:
        lines.append(f"- **Tests**: {', '.join(f'`{d}/`' for d in scan.test_dirs)}")
    else:
        lines.append("- **Tests**: not detected")

    if scan.docs_dirs:
        lines.append(f"- **Docs**: {', '.join(f'`{d}/`' for d in scan.docs_dirs)}")
    else:
        lines.append("- **Docs**: not detected")

    if scan.readme_path:
        lines.append(f"- **README**: `{scan.readme_path}`")

    return "\n".join(lines)


def _render_starting_points(scan: RepoScan) -> str:
    points: list[str] = []

    if scan.readme_path:
        points.append(f"`{scan.readme_path}` — project overview")

    for f in scan.config_files:
        if f in ("pyproject.toml", "package.json", "go.mod", "Cargo.toml", "pom.xml"):
            points.append(f"`{f}` — dependencies and project metadata")
            break

    for d in scan.source_dirs[:2]:
        points.append(f"`{d}/` — main source code")

    for d in scan.test_dirs[:1]:
        points.append(f"`{d}/` — test suite")

    if not points:
        points.append("Explore the top-level directories listed above.")

    return "\n".join(f"- {p}" for p in points)


def _render_notes(notes: list[str]) -> str:
    return "\n".join(f"- {n}" for n in notes)


