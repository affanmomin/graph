def generate_module_doc(module: ModuleMemory) -> str:
    """Generate content for ``.agent-memory/modules/<slug>.md``.

    Args:
        module: A classified :class:`~models.ModuleMemory` instance.

    Returns:
        Markdown string ready to be written to disk by ``writer.py``.
    """
    sections: list[str] = []

    # Header
    confidence_note = _confidence_label(module.confidence)
    sections.append(
        f"# Module: {module.name}\n\n"
        f"{_AUTO_GENERATED_NOTE}\n\n"
        f"**Confidence**: {module.confidence:.0%} ({confidence_note})"
    )

    # Purpose
    sections.append(render_markdown_section(
        "Purpose",
        _module_purpose(module),
    ))

    # Responsibilities — inferred from file names
    responsibilities = _infer_responsibilities(module.files)
    if responsibilities:
        sections.append(render_markdown_section(
            "Responsibilities",
            "\n".join(f"- {r}" for r in responsibilities),
        ))

    # Files
    sections.append(render_markdown_section(
        "Files",
        _render_file_list(module.files, "No source files detected."),
    ))

    # Dependencies
    if module.dependencies:
        sections.append(render_markdown_section(
            "Dependencies",
            "\n".join(f"- `{d}`" for d in sorted(module.dependencies)),
        ))

    # Dependents
    if module.dependents:
        sections.append(render_markdown_section(
            "Depended on by",
            "\n".join(f"- `{d}`" for d in sorted(module.dependents)),
        ))

    # Related tests
    sections.append(render_markdown_section(
        "Related tests",
        _render_file_list(module.tests, "No related tests detected."),
    ))

    # Risk / coupling notes
    risks = _module_risks(module)
    if risks:
        sections.append(render_markdown_section(
            "Risk and coupling notes",
            risks,
        ))

    return "\n\n".join(sections)


# ---------------------------------------------------------------------------
# Feature doc helpers
# ---------------------------------------------------------------------------


def _feature_purpose(feature: FeatureMemory) -> str:
    """Produce a one-line purpose statement for a feature doc."""
    name = feature.name
    source = _classification_source(feature.confidence)
    return (
        f"Implements the **{name}** product area "
        f"({len(feature.files)} file(s) detected; classified by {source})."
    )


def _feature_warnings(feature: FeatureMemory) -> str:
    """Return warning lines for a feature doc."""
    lines: list[str] = []
    if feature.confidence < 0.6:
        lines.append(
            f"Low confidence ({feature.confidence:.0%}): grouping is inferred from "
            "directory structure only — verify file membership manually."
        )
    if not feature.tests:
        lines.append("No test files linked to this feature. Coverage is unknown.")
    if len(feature.files) > _MAX_FILES_LISTED:
        lines.append(
            f"Large feature: {len(feature.files)} files total "
            f"(only first {_MAX_FILES_LISTED} shown above). "
            "Consider splitting into sub-features."
        )
    return "\n".join(f"- {line}" for line in lines)


def _infer_entry_points(files: list[str]) -> list[str]:
    """Return files likely to be entry points (routes, views, controllers, main)."""
    priority_stems = {
        "main", "app", "index", "server", "router", "routes",
        "views", "view", "controller", "controllers", "handler", "handlers",
        "api", "endpoint", "endpoints",
    }
    result = [
        f for f in files
        if Path(f).stem.lower() in priority_stems
    ]
    return sorted(result)[:5]


