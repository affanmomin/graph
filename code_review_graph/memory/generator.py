def _render_boundaries(scan: RepoScan) -> str:
    """Describe major code boundaries based on directory structure."""
    boundaries: list[str] = []

    if scan.source_dirs:
        for d in scan.source_dirs:
            boundaries.append(f"- **`{d}/`** — production source code")

    if scan.test_dirs:
        for d in scan.test_dirs:
            boundaries.append(f"- **`{d}/`** — test suite (separate from production code)")

    if scan.docs_dirs:
        for d in scan.docs_dirs:
            boundaries.append(f"- **`{d}/`** — documentation")

    tooling_dirs = getattr(scan, "tooling_dirs", [])
    if tooling_dirs:
        for d in tooling_dirs:
            boundaries.append(f"- **`{d}/`** — tooling / scripts")

    # Monorepo signal: multiple conventional dirs
    apps_dirs = [d for d in scan.top_level_dirs if d.lower() in ("apps", "packages", "services")]
    if apps_dirs:
        for d in apps_dirs:
            boundaries.append(f"- **`{d}/`** — multiple apps/packages (possible monorepo)")

    if not boundaries:
        boundaries.append(
            "- No clear boundaries detected. "
            "The codebase may be structured at the root level."
        )

    return "\n".join(boundaries)


def _render_layout(scan: RepoScan) -> str:
    """Infer and describe the high-level architectural layout."""
    lines: list[str] = []

    # Monorepo
    if any(d.lower() in ("apps", "packages", "services") for d in scan.top_level_dirs):
        lines.append("**Pattern**: Monorepo (multiple apps or packages in one repo)")
    elif scan.source_dirs:
        lines.append("**Pattern**: Single-application repo with dedicated source directory")
    else:
        lines.append("**Pattern**: Flat or non-standard layout")

    # Framework-specific layout hints
    if "Django" in scan.framework_hints:
        lines.append("**Django layout**: expect apps as subdirectories, `settings.py`, `urls.py`")
    if "FastAPI" in scan.framework_hints:
        lines.append("**FastAPI layout**: expect `main.py` / `app.py`, routers, models, schemas")
    if "Next.js" in scan.framework_hints:
        lines.append("**Next.js layout**: expect `pages/` or `app/`, `public/`, `components/`")
    if "React" in scan.framework_hints and "Next.js" not in scan.framework_hints:
        lines.append("**React layout**: expect `src/components/`, `src/hooks/`, `src/pages/`")

    if not lines:
        lines.append("Layout could not be confidently inferred from directory structure alone.")

    return "\n".join(lines)


def _render_coupling_notes(scan: RepoScan) -> str:
    """Surface coupling or ambiguity worth flagging."""
    notes: list[str] = []

    if len(scan.languages) > 2:
        notes.append(
            f"Multi-language repo ({', '.join(scan.languages)}). "
            "Check language boundaries before refactoring."
        )

    if not scan.test_dirs:
        notes.append(
            "No test directory detected. Test coverage is unknown — be cautious with changes."
        )

    if scan.confidence < 0.7:
        notes.append(
            f"Low-confidence scan ({scan.confidence:.0%}). "
            "Structural signals were weak; verify key locations manually."
        )

    # Pass through any scanner-generated notes
    for note in scan.notes:
        if note not in notes:
            notes.append(note)

    return "\n".join(f"- {n}" for n in notes) if notes else ""


def _render_inspect_first(
    scan: RepoScan,
    graph_signals: "ArchitectureGraphSignals | None" = None,
) -> str:
    """Files/dirs worth looking at first when starting a task.

    When *graph_signals* are provided, uses real graph-derived entry-point
    files instead of directory-name guesses.
    """
    items: list[str] = []

    if graph_signals and graph_signals.key_files:
        # Graph-grounded: list actual high-signal files
        for fp, desc in graph_signals.key_files[:5]:
            items.append(f"`{fp}` — {desc}")
    else:
        # Heuristic fallback: source dirs + key files within them
        for d in scan.source_dirs[:3]:
            items.append(f"`{d}/` — main source code")
        # Also surface well-known key files that are likely high-signal
        _KEY_FILE_LABELS: list[tuple[str, str]] = [
            ("cli.py", "CLI entry point"),
            ("main.py", "application entry point"),
            ("app.py", "application entry point"),
            ("server.py", "server entry point"),
            ("parser.py", "parser implementation"),
            ("graph.py", "graph engine"),
            ("tools.py", "tool/API implementations"),
            ("index.py", "module public interface"),
        ]
        key_files_found: list[str] = []
        for src_dir in scan.source_dirs:
            for fname, label in _KEY_FILE_LABELS:
                candidate = f"{src_dir}/{fname}"
                if (scan.repo_root / candidate).exists() and candidate not in key_files_found:
                    key_files_found.append(candidate)
                    if len(key_files_found) >= 3:
                        break
            if len(key_files_found) >= 3:
                break
        for kf_path in key_files_found:
            # Find the label for this file
            fname = Path(kf_path).name
            label = next((lbl for fn, lbl in _KEY_FILE_LABELS if fn == fname), "key file")
            items.append(f"`{kf_path}` — {label}")

    for cfg in ("pyproject.toml", "package.json", "go.mod", "Cargo.toml"):
        if cfg in scan.config_files:
            items.append(f"`{cfg}` — project metadata and dependencies")
            break

    if scan.readme_path:
        items.append(f"`{scan.readme_path}` — project intent and setup")

    for d in scan.test_dirs[:1]:
        items.append(f"`{d}/` — to understand expected behaviour")

    if not items:
        items.append("No strong starting points detected — explore top-level directories.")

    return "\n".join(f"- {i}" for i in items)


