def _module_purpose(module: ModuleMemory) -> str:
    """Produce a one-line purpose statement for a module doc."""
    name = module.name
    source = _classification_source(module.confidence)
    return (
        f"Provides the **{name}** package/module boundary "
        f"({len(module.files)} file(s); classified by {source})."
    )


def _infer_responsibilities(files: list[str]) -> list[str]:
    """Infer likely responsibilities from the file stems in this module."""
    # Map common file stems to responsibility descriptions
    stem_hints: dict[str, str] = {
        "models": "data models / ORM definitions",
        "model": "data model definitions",
        "views": "view / template rendering",
        "view": "view / template rendering",
        "routes": "URL routing / endpoint registration",
        "router": "request routing",
        "controller": "request handling / business dispatch",
        "controllers": "request handling / business dispatch",
        "service": "business logic / service layer",
        "services": "business logic / service layer",
        "middleware": "request/response middleware",
        "schema": "data schema / validation",
        "schemas": "data schema / validation",
        "serializer": "data serialisation",
        "serializers": "data serialisation",
        "utils": "shared utilities / helpers",
        "helpers": "shared helper functions",
        "config": "configuration management",
        "settings": "application settings",
        "migrations": "database schema migrations",
        "tasks": "background task definitions",
        "worker": "background worker / job processing",
        "client": "external API / service client",
        "main": "application entrypoint",
        "app": "application setup / factory",
        "index": "module public interface",
        "types": "type definitions / interfaces",
        "interfaces": "interface / contract definitions",
    }
    found: list[str] = []
    seen: set[str] = set()
    for f in sorted(files):
        stem = Path(f).stem.lower()
        if stem in stem_hints and stem_hints[stem] not in seen:
            found.append(stem_hints[stem])
            seen.add(stem_hints[stem])
    return found[:6]  # cap to avoid bloat


def _module_risks(module: ModuleMemory) -> str:
    """Return risk/coupling notes for a module doc."""
    lines: list[str] = []
    if module.confidence < 0.6:
        lines.append(
            f"Low confidence ({module.confidence:.0%}): boundary was inferred, "
            "not derived from explicit package structure."
        )
    if len(module.files) > _MAX_FILES_LISTED:
        lines.append(
            f"Large module: {len(module.files)} files. "
            "High file count may indicate coupling or missed sub-module boundaries."
        )
    if module.dependents:
        lines.append(
            f"This module is depended on by {len(module.dependents)} other module(s): "
            f"{', '.join(sorted(module.dependents)[:3])}. "
            "Changes here have broad impact."
        )
    return "\n".join(f"- {line}" for line in lines)


# ---------------------------------------------------------------------------
# Shared rendering helpers
# ---------------------------------------------------------------------------


def _render_file_list(files: list[str], empty_msg: str) -> str:
    """Render a sorted list of files as markdown bullets, truncating if large."""
    if not files:
        return empty_msg
    shown = sorted(files)[:_MAX_FILES_LISTED]
    lines = [f"- `{f}`" for f in shown]
    if len(files) > _MAX_FILES_LISTED:
        lines.append(f"- _…and {len(files) - _MAX_FILES_LISTED} more_")
    return "\n".join(lines)


def _confidence_label(confidence: float) -> str:
    if confidence >= 0.85:
        return "high — derived from explicit package structure"
    if confidence >= 0.65:
        return "medium — inferred from directory naming"
    return "low — heuristic grouping, verify manually"


def _classification_source(confidence: float) -> str:
    if confidence >= 0.85:
        return "explicit package boundary"
    if confidence >= 0.65:
        return "directory name heuristic"
    return "weak heuristic (low confidence)"


